S, N = input().split()
width = len(S)
N = int(N)

minValue = int('1' + '0' * (width - 1))
minimum = minValue // N

while True:
    D = N * minimum
    print(D)
    isEqual = False

    for index, letter in enumerate(S):
        if letter != '?':
            if str(D)[index] != letter:
                isEqual = False
                break
            else:
                isEqual = True
    
    if isEqual == True:
        print(D)
        break

    minimum += 1

    if len(str(D)) > width:
        print('*')

